package com.hugokindel.common.cli.print.style;

/** ANSI escape sequences to use in output. */
public class Style {
    /** ANSI escape sequence to reset all values. */
    public static final String Reset = "\u001B[0m";

    /** ANSI escape sequence to use bold styling. */
    public static final String Bold = "\u001B[1m";

    /** ANSI escape sequence to use underline styling. */
    public static final String Underline = "\u001B[4m";
}
